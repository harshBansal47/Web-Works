const operator = prompt("enter any mathematical operator from +,-,/,*");
 switch(operator){
   case "+":
   console.log('add');
   break;
   case "-":
   console.log('substract');
   break;
   case "*":
   console.log('multiply');
   break;
   case "/":
   console.log('division');
   break;
   default:
   console.log('undetermined');
   break;
 }
